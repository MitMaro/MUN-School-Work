public class Cast{
    /**
     * @param args
     */    
   public static void main(String[] args){
	double test = 23423874.223847238;

   	System.out.println("As a double: " + test);
   	System.out.println("As a float: " + (float)test);
   	System.out.println("As a long: " + (long)test);
   	System.out.println("As a int: " + (int)test);
   	System.out.println("As a char: " + (char)test);
   	System.out.println("As a short: " + (short)test);
   	System.out.println("As a byte: " + (byte)test);

   } // method main
} // class Cast
